mkdir -p "$RFC_DIR/accepted"
mkdir -p "$RFC_DIR/implemented"
mkdir -p "$RFC_DIR/archived"

# ==============================================================================
# 获取下一个 RFC 编号
# ==============================================================================
get_next_rfc_number() {
    local max_num=0

    for file in $(find "$RFC_DIR" -name "RFC-*.md" 2>/dev/null); do
        num=$(basename "$file" | grep -oE 'RFC-([0-9]+)' | cut -d'-' -f2)
        if [ "$num" -gt "$max_num" ]; then
            max_num=$num
        fi
    done

    printf "%04d" $((max_num + 1))
}

NEXT_RFC_NUM=$(get_next_rfc_number)

# ==============================================================================
# 交互式收集信息
# ==============================================================================
echo "📋 请输入 RFC 基本信息..."
echo ""

read -p "🔤 RFC 标题 (Title): " RFC_TITLE
read -p "👤 作者 (Author): " RFC_AUTHOR
read -p "🔖 所属领域 (Domain) [架构/安全/性能/API/其他]: " RFC_DOMAIN
read -p "📅 目标版本 (Target Version): " RFC_TARGET_VERSION

RFC_STATUS="Draft"
RFC_CREATED=$(date '+%Y-%m-%d')
RFC_UPDATED=$(date '+%Y-%m-%d')

# ==============================================================================
# 生成 RFC 文件
# ==============================================================================
RFC_FILENAME="RFC-$NEXT_RFC_NUM-$(echo "$RFC_TITLE" | tr '[:upper:]' '[:lower:]' | tr ' ' '-' | sed 's/[^a-z0-9-]//g').md"
RFC_PATH="$RFC_DIR/draft/$RFC_FILENAME"

cat > "$RFC_PATH" << EOF
---
rfc: $NEXT_RFC_NUM
title: $RFC_TITLE
author: $RFC_AUTHOR
status: $RFC_STATUS
created: $RFC_CREATED
updated: $RFC_UPDATED
domain: $RFC_DOMAIN
target_version: $RFC_TARGET_VERSION
---

# RFC-$NEXT_RFC_NUM: $RFC_TITLE

| 字段 | 值 |
|------|----|
| RFC 编号 | $NEXT_RFC_NUM |
| 标题 | $RFC_TITLE |
| 作者 | $RFC_AUTHOR |
| 状态 | Draft 📝 |
| 创建日期 | $RFC_CREATED |
| 最后更新 | $RFC_UPDATED |
| 所属领域 | $RFC_DOMAIN |
| 目标版本 | $RFC_TARGET_VERSION |

---

